  handleInput(e) {
    // 1 获取输入框的值
    const { value } = e.detail;
    console.log(value);
    // 2 检测合法性
    if (!value.trim()) {
      this.setData({
        isShow: false,
        isFocus: false,
      });
      // 值不合法
      return;
    }
    // 3 清空之前的数据
    this.resetData();

    // 4 准备发送请求获取数据
    var q = "param.q";

    this.setData({
      isFocus: true,
      isShow: true,
      [q]: value,
    });
    let { param } = this.data;
    clearTimeout(this.TimeId);
    this.TimeId = setTimeout(() => {
      this.qsearch(param);
    }, 1000);
  },
  // 发送请求获取搜索建议 数据
  async qsearch(param) {
    // console.log(param);
    const res = await request({ url: "/search", param });
    console.log(res.data);
    // 获取现在已有数据数据
    let { bookList } = this.data;
    // 拼接后才不会被新数据覆盖
    bookList = bookList.concat(res.data.books);
    // let bookList = res.data.books;
    let bookNum = res.data.total;

    this.setData({
      bookList,
      bookNum,
    });
  },
