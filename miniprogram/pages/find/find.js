  onShow() {
    // 看是否有查询缓存
    let tag = wx.getStorageSync("classifytag");
    // 有，准备查询
    if (tag) {
      // 清除现有数据
      this.resetData();

      let q = "param.q";
      this.setData({
        // param数据
        [q]: tag,
        // 搜索框设定关键词
        inpValue: tag,
        isShow: true,
      });
      let { param } = this.data;
      // 查询
      this.qsearch(param);

      // 删除本地缓存，防止二次读取
      wx.removeStorage({
        key: "classifytag",
        success(res) {
          console.log("删除");
        },
      });
    }
  },
